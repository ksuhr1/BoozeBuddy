public class DrinkData {

    String itemName;
    String itemId;
    String brandName;
    String Calories;
    String servingSize;
    String Quantity;
    String totalFat;
    String CaloriesFromFat;
    String SaturatedFat;
    String Cholesterol;
    String TotalCarbs;
    String TotalSugar;
    String DietaryFiber;
    String Protein;

    public DrinkData() {

    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getBrandName() {
        return brandName;
    }

    public String getItemId() {
        return itemId;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getCalories() {
        return Calories;
    }

    public void setCalories(String Calories) {
        this.Calories = Calories;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    public String getServingSize() {
        return servingSize;
    }

    public void setServingSize(String servingSize) {
        this.servingSize = servingSize;
    }

    public String getQuantity() {
        return Quantity;
    }

    public void setQuantity(String Quantity) {
        this.Quantity = Quantity;
    }

    public String getTotalFat() {
        return totalFat;
    }

    public void setTotalFat(String totalFat) {
        this.totalFat = totalFat;
    }

    public String getCaloriesFromFat() {
        return CaloriesFromFat;
    }

    public String getSaturatedFat() {
        return SaturatedFat;
    }

    public void setSaturatedFat(String SaturatedFat) {
        this.SaturatedFat = SaturatedFat;
    }

    public String getCholesterol() {
        return Cholesterol;
    }

    public void setCholesterol(String Cholesterol) {
        this.Cholesterol = Cholesterol;
    }

    public String getTotalCarbs() {
        return TotalCarbs;
    }

    public void setTotalCarbs(String TotalCarbs) {
        this.TotalCarbs = TotalCarbs;
    }

    public String getTotalSugar() {
        return TotalSugar;
    }

    public void setTotalSugar(String TotalSugar) {
        this.TotalSugar = TotalSugar;
    }

    public String DietaryFiber() {
        return DietaryFiber;
    }

    public void DietaryFiber(String DietaryFiber) {
        this.DietaryFiber = DietaryFiber;
    }

    public String Protein() {
        return Protein;
    }

    public void setProtein(String Protein) {
        this.Protein = Protein;
    }
}
